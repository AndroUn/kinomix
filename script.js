function openForm() {
  document.getElementById("myForm").style.display = "block";
}
function closeForm() {
  document.getElementById("myForm").style.display = "none";
}
function openReg() {
  document.getElementById("myReg").style.display = "block";
  document.getElementById("myForm").style.display = "none";
}
function closeReg() {
  document.getElementById("myReg").style.display = "none";
}
